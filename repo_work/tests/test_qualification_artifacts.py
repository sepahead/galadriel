def relocate_sbom_workspace_references(
    value: object,
    *,
    old_root: str,
    new_root: str,
) -> object:
    if isinstance(value, dict):
        return {
            key: relocate_sbom_workspace_references(
                item, old_root=old_root, new_root=new_root
            )
            for key, item in value.items()
        }
    if isinstance(value, list):
        return [
            relocate_sbom_workspace_references(
                item, old_root=old_root, new_root=new_root
            )
            for item in value
        ]
    if isinstance(value, str):
        return value.replace(
            f"path+file://{old_root}/crates/",
            f"path+file://{new_root}/crates/",
        )
    return value


