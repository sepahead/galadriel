def lockfile_bytes(packages: list[dict[str, object]]) -> bytes:
    lines = [
        "# This file is automatically @generated by Cargo.",
        "version = 4",
        "",
    ]
    for package in packages:
        lines.extend(
            [
                "[[package]]",
                f"name = {json.dumps(package['name'])}",
                f"version = {json.dumps(package['version'])}",
            ]
        )
        if package.get("source") is not None:
            lines.append(f"source = {json.dumps(package['source'])}")
        if package.get("checksum") is not None:
            lines.append(f"checksum = {json.dumps(package['checksum'])}")
        dependencies = package.get("dependencies", [])
        if dependencies:
            encoded = ", ".join(json.dumps(item) for item in dependencies)
            lines.append(f"dependencies = [{encoded}]")
        lines.append("")
    return "\n".join(lines).encode()


