def license_inventory_expectations(
    graph: ValidatedCargoGraph,
    inventory: dict[str, object],
) -> tuple[int, str, str]:
    def semantic_package_id(package: CargoPackage) -> str:
        if package.workspace:
            return f"workspace+crates/{package.name}#{package.name}@{package.version}"
        return package.package_id

    packages = {
        (
            package.name,
            package.version,
            package.package_id.rsplit("#", 1)[0],
        ): package
        for package in graph.packages
    }
    rows: list[dict[str, object]] = []
    assignment_count = 0
    for identity, record_value in inventory.items():
        name, version, source = identity.split(" ", 2)
        package = packages[(name, version, source)]
        record = record_value
        if not isinstance(record, dict):
            raise AssertionError("test inventory record is not an object")
        licenses = record["licenses"]
        if not isinstance(licenses, list):
            raise AssertionError("test inventory licenses are not a list")
        assignment_count += len(licenses)
        rows.append(
            {
                "licenses": sorted(licenses),
                "package_id": semantic_package_id(package),
            }
        )
    rows.sort(key=lambda row: row["package_id"])
    package_ids = sorted(row["package_id"] for row in rows)

    def canonical(value: object) -> bytes:
        return json.dumps(
            value,
            ensure_ascii=False,
            sort_keys=True,
            separators=(",", ":"),
        ).encode()

    return (
        assignment_count,
        hashlib.sha256(canonical(package_ids)).hexdigest(),
        hashlib.sha256(canonical(rows)).hexdigest(),
    )


