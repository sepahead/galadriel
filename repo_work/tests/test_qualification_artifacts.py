class JsonBoundsTest(unittest.TestCase):
    def test_rejects_deep_json_before_recursive_parsing(self) -> None:
        depth = artifacts.MAX_JSON_DEPTH + 1
        payload = b"[" * depth + b"0" + b"]" * depth
        with (
            mock.patch.object(
                artifacts.json,
                "loads",
                side_effect=AssertionError("parser must not run"),
            ),
            self.assertRaisesRegex(ReviewError, "JSON depth bound"),
        ):
            artifacts._load_json_bytes(
                payload,
                len(payload),
                "deep JSON fixture",
            )

    def test_rejects_excess_json_nodes_before_parsing(self) -> None:
        payload = b"[" + b",".join([b"0"] * 8) + b"]"
        with (
            mock.patch.object(artifacts, "MAX_JSON_NODES", 8),
            mock.patch.object(
                artifacts.json,
                "loads",
                side_effect=AssertionError("parser must not run"),
            ),
            self.assertRaisesRegex(ReviewError, "JSON node bound"),
        ):
            artifacts._load_json_bytes(
                payload,
                len(payload),
                "wide JSON fixture",
            )

    def test_bounds_parser_error_text_and_catches_recursion(self) -> None:
        detail = "sensitive-parser-detail-" * 1_000
        context = "large-context-" * 1_000
        with (
            mock.patch.object(
                artifacts.json,
                "loads",
                side_effect=RecursionError(detail),
            ),
            self.assertRaises(ReviewError) as caught,
        ):
            artifacts._load_json_bytes(b"{}", 2, context)
        message = str(caught.exception)
        self.assertNotIn(detail, message)
        self.assertLessEqual(
            len(message),
            artifacts.MAX_JSON_CONTEXT_CHARS + artifacts.MAX_JSON_ERROR_CHARS + 32,
        )

    def test_ignores_json_structure_inside_strings(self) -> None:
        payload = b'{"value":"[{\\"nested\\":true}]"}'
        self.assertEqual(
            artifacts._load_json_bytes(payload, len(payload), "string fixture"),
            {"value": '[{"nested":true}]'},
        )

    def test_omits_a_duplicate_key_from_the_error(self) -> None:
        duplicate_key = "private-key-material-" * 1_000
        payload = (
            "{" + json.dumps(duplicate_key) + ":0," + json.dumps(duplicate_key) + ":1}"
        ).encode()
        with self.assertRaisesRegex(ReviewError, "duplicate JSON key") as caught:
            artifacts._load_json_bytes(payload, len(payload), "duplicate fixture")
        self.assertNotIn(duplicate_key, str(caught.exception))


