def crate_archive(
    *,
    duplicate_source: bool = False,
    extra_source: bool = False,
    include_lock: bool = False,
    omit_source: bool = False,
    substitute_source: bool = False,
    symbolic_source: bool = False,
    vcs_commit: str = COMMIT,
) -> bytes:
    prefix = f"galadriel-core-{VERSION}/"
    files = [
        ("Cargo.toml", GENERATED_CRATE_MANIFEST),
        ("Cargo.toml.orig", ORIGINAL_CRATE_MANIFEST),
        (
            ".cargo_vcs_info.json",
            encode_json(
                {
                    "git": {"sha1": vcs_commit},
                    "path_in_vcs": "crates/galadriel-core",
                }
            ),
        ),
    ]
    if not omit_source:
        files.append(
            (
                "src/lib.rs",
                b"substituted source\n" if substitute_source else CRATE_SOURCE,
            )
        )
    if duplicate_source:
        files.append(("src/lib.rs", b"duplicate\n"))
    if extra_source:
        files.append(("src/hidden.rs", b"pub fn hidden() {}\n"))
    if include_lock:
        files.append(("Cargo.lock", b"version = 4\n"))
    tar_payload = io.BytesIO()
    with tarfile.open(
        fileobj=tar_payload, mode="w", format=tarfile.USTAR_FORMAT
    ) as archive:
        for relative, content in files:
            member = tarfile.TarInfo(prefix + relative)
            member.uid = 0
            member.gid = 0
            member.mode = 0o644
            if symbolic_source and relative == "src/lib.rs":
                member.type = tarfile.SYMTYPE
                member.linkname = "../../outside"
                member.size = 0
                archive.addfile(member)
            else:
                member.size = len(content)
                archive.addfile(member, io.BytesIO(content))
    raw_tar = tar_payload.getvalue()
    payload_end = 0
    with tarfile.open(fileobj=io.BytesIO(raw_tar), mode="r:") as archive:
        for member in archive:
            payload_end = max(
                payload_end,
                member.offset_data + ((member.size + 511) // 512) * 512,
            )
    raw_tar = raw_tar[:payload_end] + bytes(1024)
    compressed = io.BytesIO()
    with gzip.GzipFile(filename="", mode="wb", fileobj=compressed, mtime=0) as stream:
        stream.write(raw_tar)
    return compressed.getvalue()


